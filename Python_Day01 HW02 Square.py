# Drawing Square using two colors:

import turtle
m = turtle.Pen()
m . shape("turtle")
m . color("red","blue")
m . begin_fill()
m . forward(100)
m . left(90)
m . forward(100)
m . left(135)
m . forward(140)
m . backward(140)
m . end_fill()
m . color("red","green")
m . begin_fill()
m . right (45)
m . forward(100)
m . left(90)
m . forward(100)
m . left(90)
m . end_fill()

